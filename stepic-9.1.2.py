n = input()
print(n[2])     # третий символ этой строки;
print(n[-2:-1])# предпоследний символ этой строки;
print(n[0:5])# первые пять символов этой строки;
print(n[:-2])# всю строку, кроме последних двух символов;
print(n[::2])# все символы с четными индексами;
print(n[1::2])# все символы с нечетными индексами;
print(n[::-1])# все символы в обратном порядке;
print(n[-1::-2])# все символы строки через один в обратном порядке, начиная с последнего;




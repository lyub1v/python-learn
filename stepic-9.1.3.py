n = input()
print(len(n))              # кол-во символов
print(n + n + n)       # 3 раза исходная строка
print(n[0])                # 1-ый символ строки
print(n[0:3])              # первые 3 символа
print(n[-3:])              # последние 3 символа
print(n[::-1])             #обратный порядок
print(n[1:][:-1])          #удален 1 и последний символ






